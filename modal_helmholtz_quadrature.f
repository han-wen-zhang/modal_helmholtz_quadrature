c
c     Generalized Gaussian quadrature rules for the near-singular
c     integrand on the steepest-descent contour gamma_1, used in the
c     evaluation of the azimuthal Fourier modes of the 3D Helmholtz
c     Green's function described in
c
c       H. Zhang, "Fast Evaluation of the Azimuthal Fourier Modes of
c       the 3D Helmholtz Green's Function and Their Derivatives,"
c       2026.
c
c     This file contains 18 rules, load_quad1 through load_quad18,
c     covering dyadic ranges of the near-singular parameter bm
c     (denoted beta_- in the paper):
c
c       load_quad1:    bm in [2^-5 ,  2^0 ]
c       load_quad2:    bm in [2^-10,  2^-5]
c       ...
c       load_quad18:   bm in [2^-90, 2^-85]   (~ 8e-28)
c
c     Each rule is a generalized Gaussian quadrature on [0,1] that
c     integrates the five families
c
c         /1
c         |  P_n(t) / [ sqrt(t^2 - 2i bm) (t^2 - i bm)^j ] dt,
c         0
c
c         j = 0, 1, 2, 3, 4,
c
c     exactly for all polynomials P_n of degree n = 0, 1, ..., 63.
c
c     Each subroutine loads a single rule:
c
c       call load_quadk(xlist, wlist, nn)
c
c     where xlist and wlist are real*8 arrays of length at least 130
c     supplied by the caller, and nn is an integer that returns the
c     actual node count.  On exit, (xlist(i), wlist(i)) for
c     i = 1, ..., nn are the nodes and weights on [0,1].  The runtime
c     caller may rescale to [0, tau] for any tau > 0.
c
c     The well-separated regime (bm > 2^0) is not covered here; a
c     standard Gauss-Legendre rule on [0,1] suffices in that case.
c
c     The rules were constructed using the nonlinear optimization
c     procedure of Bremer, Gimbutas, and Rokhlin,
c     "A nonlinear optimization procedure for generalized Gaussian
c     quadratures", SIAM J. Sci. Comput. 32 (2010), 1761-1788.
c     See also https://github.com/JamesCBremerJr/GGQ for code.
c
